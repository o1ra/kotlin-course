fun clothes(temp: Int){
    when {
        temp < -30 || temp > 35 -> {
            println("не выходите из дома")
        }
        temp < 10 -> {
            println("куртка и шапка")
        }
        temp in 10..18 -> {
            println("ветровка")
        }
        temp > 18  -> println("футболка и шорты")

    }
}

