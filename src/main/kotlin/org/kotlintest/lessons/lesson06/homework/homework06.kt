// исходя из длины маршрута. Если маршрут до 1 км - "пешком", до 5 км - "велосипед", иначе - "автотранспорт".

//если использвать конструкцию if:
fun bestWay(length: Double) {
    if (length < 1.0) println("Пешком")
    else if (length in 1.0..<5.0) println("Велосипед")
    else println("Автотранспорт")
}

// если использовать констркуцию when:
fun bestWay2(length: Double) {
    when {
        length < 1.0 -> println("Пешком")
        length in 1.0..<5.0 -> println("Велосипед")
        else -> println("Автотранспорт")
    }
}


//Задание 4: "Расчет бонусных баллов"
