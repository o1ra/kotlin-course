//Задание 6: "Конвертация температуры"
//Создайте функцию, которая конвертирует температуру из градусов Цельсия в
// Фаренгейты и наоборот в зависимости от указанной единицы измерения (C/F).
// Единицу измерения нужно передать вторым аргументом функции. Несколько аргументов
// передаются через запятую. Распечатай в консоль результат конвертации с добавлением
// единицы измерения. Чтобы добавить единицу измерения после результата используй функцию
// печати без переноса строки print("C") или print("F").

fun temp(gradus: Double, type: String) {
    when (type) {
        "C" -> {
            print(gradus * 1.8 + 32)
            print(type)
        }
        "F" -> {
            print((gradus - 32) / 1.8)
            print(type)
        }
        else -> {
            println("Неизвествный тип")
        }
    }
}

//Задание 7: "Подбор одежды по погоде"
