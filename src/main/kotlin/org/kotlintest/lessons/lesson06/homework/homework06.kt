//Задание 2: "Расчет возраста питомца"
//Создайте функцию, которая преобразует возраст собаки в "человеческие" годы.
// До 2 лет каждый год собаки равен 10.5 человеческим годам, после - каждый год равен 4
// человеческим годам. Результат распечатай в консоль.
fun dogAge(age: Int) {
    if (age in 0..2) {
        println(age * 10.5)
    } else if (age > 2) {
        println((age - 2) * 4 + 21)
    } else {
        println("Такого возраста не существует")
    }
}


