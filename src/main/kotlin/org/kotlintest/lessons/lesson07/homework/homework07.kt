    println("14. В цикле for от 1 до 10 используйте continue, чтобы пропустить четные числа.")
    for (i in 1..10){
        if (i % 2 == 0){
            continue
        }
        println(i)
    }


//15. Напишите цикл while, который выводит числа от 1 до 10, но пропускает числа, кратные 3.
    println("15. Напишите цикл while, который выводит числа от 1 до 10, но пропускает числа, кратные 3.")
    var i15 = 0
    while (i15 < 10) {
        if (++i15 % 3 == 0){
            continue

        }
        println(i15)
    }
}